package fr.gervais.rest.app.models;

import java.util.UUID;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;

import org.hibernate.annotations.GenericGenerator;

@Entity(name = "Profile")
public class User {
	
	//Attributes.
	@Id
	@GeneratedValue(generator = "UUID")
	@GenericGenerator(
			name = "UUID",
			strategy = "org.hibernate.id.UUIDGenerator"
			)
	private final UUID id = null;	//This value will be auto-generated by the repository interface.

	private String login;
	private String password;
	private String name;
	private String mail;
	private String phone;

	//Constructors.
	
	public User() {
		
	}

	public User(String login, String password, String name, String mail, String phone) {
		super();
		this.login = login;
		this.password = password;
		this.name = name;
		this.mail = mail;
		this.phone = phone;
	}

	//Get'er & Set'er Methods.
	
	public UUID getid() {
		return this.id;
	}

	public String getlogin() {
		return this.login;
	}

	public String getpassword() {
		return this.password;
	}
	
	public String getname() {
		return this.name;
	}

	public String getmail() {
		return this.mail;
	}

	public String getphone() {
		return this.phone;
	}

	public void setlogin(String login) {
		this.login = login;
	}

	public void setpassword(String password) {
		this.password = password;
	}
	
	public void setname(String name) {
		this.name = name;
	}

	public void setmail(String mail) {
		this.mail = mail;
	}

	public void setphone(String phone) {
		this.phone = phone;
	}
	
	//Custom Methods.
	
}
